public class B2b {
    public static void main(String[] args) {
        int octal = 032; //8 진수
        System.out.println(octal);
        System.out.printf("%o", 10); //12 출력

        int decimal = 26; //10진수
        int hex = 0x1A; // 16진수
        int binary = 0b11010; // 2진수

        System.out.println(decimal);
        System.out.println(hex);
        System.out.println(binary);

    }
}
